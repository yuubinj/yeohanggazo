package com.travel.model;

public class ScrapDTO {
	private String userId;
	private String apiId;
	private String apiTypeId;
	private String scrapName;
	private String scrapAddr;
	private String scrapImg;
	private String scrap_date;
	private String locationName;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getApiId() {
		return apiId;
	}
	public void setApiId(String apiId) {
		this.apiId = apiId;
	}
	public String getApiTypeId() {
		return apiTypeId;
	}
	public void setApiTypeId(String apiTypeId) {
		this.apiTypeId = apiTypeId;
	}
	public String getScrapName() {
		return scrapName;
	}
	public void setScrapName(String scrapName) {
		this.scrapName = scrapName;
	}
	public String getScrapAddr() {
		return scrapAddr;
	}
	public void setScrapAddr(String scrapAddr) {
		this.scrapAddr = scrapAddr;
	}
	public String getScrapImg() {
		return scrapImg;
	}
	public void setScrapImg(String scrapImg) {
		this.scrapImg = scrapImg;
	}
	public String getScrap_date() {
		return scrap_date;
	}
	public void setScrap_date(String scrap_date) {
		this.scrap_date = scrap_date;
	}
	public String getLocationName() {
		return locationName;
	}
	public void setLocationName(String locationName) {
		this.locationName = locationName;
	}

}
